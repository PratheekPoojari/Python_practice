score = int(input("Enter your score:",))

if 90 <= score < 100:
    print("You have secured Grade 'A'")
elif 80 <= score < 90:
    print("You have secured Grade 'B'")
elif 70 <= score < 80:
    print("You have secured Grade 'C'")
elif 60 <= score < 70:
    print("You have secured Grade 'D'")
else:
    print("You have secured Grade 'F'")

